namespace CallingMethod
{
    internal class MathOperation
    {

        // This class contains methods to perform mathematical operations
        public class MathOperations
        {
            // Method to double the input number
            public int Double(int number)
            {
                return number * 2; // Multiplies the input number by 2
            }

            // Method to square the input number
            public int Square(int number)
            {
                return number * number; // Returns the square of the input number
            }

            // Method to increment the input number by 10
            public int AddTen(int number)
            {
                return number + 10; // Adds 10 to the input number
            }
        }
    }
}

